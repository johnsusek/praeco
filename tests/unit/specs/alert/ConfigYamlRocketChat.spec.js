realert:
  minutes: 5
rocket_chat_attach_kibana_discover_url: false
rocket_chat_emoji_override: ":ghost:"
rocket_chat_kibana_discover_color: "#ec4b98"
rocket_chat_kibana_discover_title: "Discover in Kibana"
